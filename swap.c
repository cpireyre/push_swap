#include "header.h"

void	sa(t_ps *ps)
{
	if (ps->size_a >= 2)
		ft_swap_int(&((ps->a)[0]), &((ps->a)[1]));
}

void	sb(t_ps *ps)
{
	if (ps->size_b >= 2)
		ft_swap_int(&((ps->b)[0]), &((ps->b)[1]));
}
